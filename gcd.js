/**
 * Функция возвращает наибольший общий делитель двух неотрицательных чисел
 * @param {number} a - первое неотрицательное число
 * @param {number} b - второе неотрицательное число
 * @returns {number} - наибольший общий делитель чисел a и b
 * @throws {Error} - если аргументы не являются целыми неотрицательными числами
 */
function gcd(a, b) {
  // Проверка входных данных
  if (typeof a !== 'number' || !Number.isInteger(a) || a < 0) {
    throw new Error('Аргумент a должен быть целым неотрицательным числом');
  }

  if (typeof b !== 'number' || !Number.isInteger(b) || b < 0) {
    throw new Error('Аргумент b должен быть целым неотрицательным числом');
  }

  // Алгоритм Евклида
  while (b !== 0) {
    const temp = b;
    b = a % b;
    a = temp;
  }

  // Если оба числа равны 0, НОД не определен, возвращаем 0
  // или можно вернуть 0, что часто используется по договоренности
  return a;
}

// Экспорт функции для использования в модульной системе
if (typeof module !== 'undefined' && module.exports) {
  module.exports = { gcd };
}